    // 🌟 法宝二：业务方法（Actions）。用来给卡片长按时调用
    fun toggleFavorite(artistName: String) {
        val index = artistList.indexOfFirst { it.name == artistName }
        if (index != -1) {
            val current = artistList[index]
            // 把对应的收藏状态取反（true 变 false，false 变 true）
            artistList[index] = current.copy(isFavorite = !current.isFavorite)
        }
    }
